void main() {
  var numa = 5;
  var numb = 10;

  print("sum = ${numa + numb}"); // Output: sum = 15
  print("difference = ${numb - numa}"); // Output: difference = 5
  print("product = ${numa * numb}"); // Output: product = 50
  print("quotient = ${numb / numa}"); // Output: quotient = 2.0
  print("remainder = ${numb % numa}"); // Output: remainder = 0
  print("integer division = ${numb ~/ numa}"); // Output: integer division = 2
}
